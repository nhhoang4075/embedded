void DisplayLEDs(int mode)
{
    if (mode == 1)      // Running spot L
    {
        LED_Value = (0x01 << step_count);
        step_count++;
        if (step_count >= 8) step_count = 0;
    }
    else if (mode == 2) // Running spot R
    {
        LED_Value = (0x80 >> step_count);
        step_count++;
        if (step_count >= 8) step_count = 0;
    }
    else if (mode == 3) // Flash
    {
        LED_Value = ~LED_Value;
    }
    else if (mode == 4) // Spot Bumber
    {
        if (spot_dir == 0) {
            LED_Value = (0x80 >> spot_index) | (0x01 << spot_index);
            spot_index++;
            if (spot_index >= 4) { spot_dir = 1; spot_index = 3; }
        } else {
            spot_index--;
            LED_Value = (0x80 >> spot_index) | (0x01 << spot_index);
            if (spot_index <= 0) { spot_dir = 0; spot_index = 0; }
        }
    }

    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, (LED_Value & 0x80) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, (LED_Value & 0x40) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, (LED_Value & 0x20) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, (LED_Value & 0x10) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_11, (LED_Value & 0x08) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, (LED_Value & 0x04) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9,  (LED_Value & 0x02) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_8,  (LED_Value & 0x01) ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

void ProcessIrCommand(uint8_t cmd)
{
    Set7SegDisplayValue(cmd);

    if (cmd == 0x1C)        // OK
    {
        led4_state = !led4_state;
        HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14,
            led4_state ? GPIO_PIN_SET : GPIO_PIN_RESET);
    }
    else if (cmd == 0x45)   // 1 -> Running spot L
    {
        effect_mode = 1;
        step_count = 0;
    }
    else if (cmd == 0x46)   // 2 -> Running spot R
    {
        effect_mode = 2;
        step_count = 0;
    }
    else if (cmd == 0x47)   // 3 -> Flash
    {
        effect_mode = 3;
        LED_Value = 0xFF;
    }
    else if (cmd == 0x44)   // 4 -> Spot Bumber
    {
        effect_mode = 4;
        spot_index = 0;
        spot_dir = 0;
    }
}
